/**
 * @brief 求最大公约数, Greatest Common Divisor
 */
int32_t gcd(int32_t a, int32_t b)
{
    // 辗转相除法

    int32_t r;

    while (b != 0)
    {
        r = a % b;
        a = b;
        b = r;
    }

    return a;
}

/**
 * @brief 求最小公倍数, Least Common Multiple
 */
int32_t lcm(int32_t a, int32_t b)
{
    return a / gcd(a, b) * b;
}
